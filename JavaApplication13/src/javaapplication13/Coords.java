package javaapplication13;
public class Coords
{
    Integer x, y;
    
    public Coords(){}
    
    public Coords(Integer x, Integer y)
    {
        this.x = x;
        this.y = y;
    }

    public Integer getXCoord()
    {
        return x;
    }
    
    public Integer getYCoord()
    {
        return y;
    }  
}